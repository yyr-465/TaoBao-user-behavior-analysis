from 数据预处理 import process_data
import pandas as pd

def analyze_data(df):
    print("正在进行漏斗分析")
    behavior_counts = df['behavior_type'].value_counts()
    return behavior_counts
def perform_funnel_analysis(df):
    print("进行核心行为漏斗分析")
    behavior_counts = df['behavior_type'].value_counts().reindex(
        ['pv','fav','cart','buy'])
    funnel = pd.DataFrame(behavior_counts).reset_index()
    funnel.columns = ['环节',"行为总量"]
    # 3. 计算单一路径转化率 (相对于 PV)
    funnel['总转化率(%)'] = (funnel['行为总量'] / funnel['行为总量'].
                             iloc[0] * 100).round(2)

    # 4. 计算各环节之间的转化率 (当前步骤 / 上一步骤)
    # 使用 shift(1) 获取上一行的数据
    funnel['环节转化率(%)'] = (funnel['行为总量'] / funnel['行为总量'].
                               shift(1) * 100).round(2)
    funnel.fillna({'环节转化率(%)': 100.0}, inplace=True)
    # 第一行设为100%

    print(funnel)
    # 5. 关键业务洞察打印
    pv_to_buy = funnel.loc[funnel['环节'] == 'buy', '总转化率(%)'].values[0]
    print(f"\n[业务结论] 最终付费转化率 (PV -> Buy): {pv_to_buy}%")

    return funnel



